
public class CalculatorMethods 
{

	public int addNumbers (int x, int y){
		int z= x + y;
		return z;
	}
	public int subNumbers (int x, int y){
		int z= x - y;
		return z;
	}
	public int mulNumbers (int x, int y){
		int z= x * y;
		return z;
	}
	public int divNumbers (int x, int y){
		int z= x / y;
		return z;
	}
}
